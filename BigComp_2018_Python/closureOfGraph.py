n, edge = list(map(int, input().split()))
a = [[0 for i in range(n)] for j in range(n)]
for i in range(edge):
    x, y = [int(i) for i in input().split()]
    a[x-1][y-1] = 1
    a[y-1][x-1] = 1
degree = [sum(a[i]) for i in range(n)]
count = 0
flag = False
while flag == False:
    flag = True
    for i in range(n):
        for j in range(i+1, n):
            if degree[i]+degree[j]>=n and a[i][j]==0:
                a[i][j]=a[j][i]=1
                degree[i]+=1
                degree[j]+=1
                count+=1
                flag=False
print(count)